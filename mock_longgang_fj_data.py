def get_md5_num(username):
    md5_machine = hashlib.md5()
    md5_machine.update(username.encode('utf-8'))
    md5_hash_string = md5_machine.hexdigest()
    return md5_hash_string


def mock_data(q_path, qmc):
    result = pd.DataFrame(
        columns=["id", "qmc", "date", "y",
                 "area", "num_ws", "num_ys", "build_year", "distance", "avg_price"]
    )

    for date in DATE:
        value = qmc + date
        _id = get_md5_num(value)
        label = int(np.random.randint(0, 3))

        area = int(np.random.randint(50, 300))
        num_ws = int(np.random.randint(1, 4))
        num_ys = int(np.random.randint(1, max(2, num_ws)))
        build_year = int(np.random.randint(1990, 2024))
        distance = int(np.random.randint(1, 5))
        avg_price = int(np.random.randint(35000, 50000))

        data = [
            _id, qmc, date, label, area, num_ws, num_ys, build_year, distance, avg_price
        ]
        result.loc[len(result)] = data

    result.to_csv(q_path, index=False, sep=',')


